package O_Loops;

public class For {
    public static void main(String[] args) {
        //for loop
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello word" + i);

        }

        for (int i = 5; i > 0; i--) {
            System.out.println("Hello word" + i);
        }
    }
}
